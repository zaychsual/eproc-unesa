										<tr>
											<td>
												<ol>
													<li>
														<span>LINGKUP PEKERJAAN</span>
													</li>
													<p>
														<span>Penyedia yang ditunjuk berkewajiban untuk menyelesaikan pekerjaan dalam jangka waktu yang ditentukan, dengan mutu sesuai spesifikasi teknis dan harga sesuai SPK.</span>
													</p>
													<li>
														<span>HUKUM YANG BERLAKU&nbsp;</span>
													</li>
													<p>
														<span>Keabsahan, interpretasi, dan pelaksanaan SPK ini didasarkan kepada hukum Republik Indonesia.</span>
													</p>
													<li>
														<span>PENYEDIA JASA MANDIRI</span>
													</li>
													<p>
														<span>SPK ini tidak dimaksudkan untuk menciptakan hubungan hukum antara Pejabat Pembuat Komitmen (PPK) dan penyedia seperti hubungan hukum antara majikan dan buruh atau antara prinsipal dan agen. Penyedia bertanggung jawab penuh terhadap personilnya.]</span>
													</p>
													<li>
														<span>HARGA SPK</span>
													</li>
													<ol>
														<li>
															<span>Jenis kontrak yang digunakan adalah kontrak lumpsum.</span>
														</li>

														<li>
															<span>PPK membayar kepada penyedia atas pelaksanaan pekerjaan dalam SPK sebesar harga SPK.&nbsp;</span>
														</li>
														<li>
															<span>Harga SPK telah memperhitungkan keuntungan, beban pajak dan biaya overhead serta biaya asuransi yang meliputi juga biaya keselamatan dan kesehatan kerja.</span>
														</li>
														<li>
															<span>Rincian harga SPK sesuai dengan rincian yang tercantum dalam daftar kuantitas dan harga </span>
															<em>
																<span>(untuk kontrak harga satuan atau kontrak gabungan harga satuan dan lump sum)</span>
															</em>
															<span>.</span>
														</li>
													</ol>
													<li>
														<span>HAK KEPEMILIKAN</span>
													</li>
													<ol>
														<li>
															<span>PPK berhak atas kepemilikan semua barang/bahan yang terkait langsung atau disediakan sehubungan dengan jasa yang diberikan oleh penyedia kepada PPK. Jika diminta oleh PPK maka penyedia berkewajiban untuk membantu secara optimal pengalihan hak kepemilikan tersebut kepada PPK sesuai dengan hukum yang berlaku.</span>
														</li>
														<li>
															<span>Hak kepemilikan atas peralatan dan barang/bahan yang disediakan oleh PPK tetap pada PPK, dan semua peralatan tersebut harus dikembalikan kepada PPK pada saat SPK berakhir atau jika tidak diperlukan lagi oleh penyedia. Semua peralatan tersebut harus dikembalikan dalam kondisi yang sama pada saat diberikan kepada penyedia dengan pengecualian keausan akibat pemakaian yang wajar.</span>
														</li>
													</ol>
													<li>
														<span>CACAT MUTU</span>
													</li>
													<p>
														<span>PPK akan memeriksa setiap hasil pekerjaan penyedia dan memberitahukan secara tertulis penyedia atas setiap cacat mutu yang ditemukan. PPK dapat memerintahkan penyedia untuk menemukan dan mengungkapkan cacat mutu, serta menguji pekerjaan yang dianggap oleh PPK mengandung cacat mutu.&nbsp;</span>
													</p>

													<li>
														<span>PERPAJAKAN&nbsp;</span>
													</li>
													<p>
														<span>Penyedia berkewajiban untuk membayar semua pajak, bea, retribusi, dan pungutan lain yang dibebankan oleh hukum yang berlaku atas pelaksanaan SPK. Semua pengeluaran perpajakan ini dianggap telah termasuk dalam harga SPK.</span>
													</p>
													<li>
														<span>PENGALIHAN DAN/ATAU SUBKONTRAK&nbsp;</span>
													</li>
													<p>
														<span>Penyedia dilarang untuk mengalihkan dan/atau mensubkontrakkan sebagian atau seluruh pekerjaan. Pengalihan seluruh pekerjaan hanya diperbolehkan dalam hal pergantian nama penyedia, baik sebagai akibat peleburan </span>
														<em>
															<span>(merger)</span>
														</em>
														<span> atau akibat lainnya.</span>
													</p>
													<div class="page-break">
													</div>
													<li>
														<span>JADWAL</span>
													</li>
													<ol>
														<li>
															<span>SPK ini berlaku efektif pada tanggal penandatanganan oleh para pihak atau pada tanggal yang ditetapkan dalam SP (Surat Pesanan).</span>
														</li>
														<li>
															<span>Waktu pelaksanaan SPK adalah sejak tanggal mulai kerja yang tercantum dalam SP (Surat Pesanan).&nbsp;&nbsp;&nbsp;&nbsp;</span>
														</li>
														<li>
															<span>Penyedia harus menyelesaikan pekerjaan sesuai jadwal yang ditentukan.&nbsp;</span>
														</li>
														<li>
															<span>Apabila penyedia berpendapat tidak dapat menyelesaikan pekerjaan sesuai jadwal karena keadaan diluar pengendaliannya dan penyedia telah melaporkan kejadian tersebut kepada PPK, maka PPK dapat melakukan penjadwalan kembali pelaksanaan tugas penyedia dengan adendum SPK.</span>
														</li>
													</ol>
													<li>
														<span>ASURANSI&nbsp;</span>
													</li>
													<ol>
														<li>
															<span>&nbsp;Penyedia wajib menyediakan asuransi sejak Surat Pesanan (SP)&nbsp; sampai dengan tanggal selesainya pemeliharaan untuk:</span>
														</li>
													</ol>
													<ol>
														<li>
															<span>semua barang dan peralatan yang mempunyai risiko tinggi terjadinya kecelakaan, pelaksanaan pekerjaan, serta pekerja untuk pelaksanaan pekerjaan, atas segala risiko terhadap kecelakaan, kerusakan, kehilangan, serta risiko lain yang tidak dapat diduga;</span>
														</li>
														<li>
															<span>pihak ketiga sebagai akibat kecelakaan di tempat kerjanya; dan</span>
														</li>
														<li>
															<span>perlindungan terhadap kegagalan bangunan.</span>
														</li>
													</ol>
													<ol>
														<li>
															<span>&nbsp;Besarnya asuransi sudah diperhitungkan dalam penawaran dan termasuk dalam harga SPK.</span>
														</li>
													</ol>
													<li>
														<span>PENANGGUNGAN DAN RISIKO</span>
													</li>
													<ol>
														<li>
															<span>Penyedia berkewajiban untuk melindungi, membebaskan, dan menanggung tanpa batas PPK beserta instansinya terhadap semua bentuk tuntutan, tanggung jawab, kewajiban, kehilangan, kerugian, denda, gugatan atau tuntutan hukum, proses pemeriksaan hukum, dan biaya yang dikenakan terhadap PPK beserta instansinya (kecuali kerugian yang mendasari tuntutan tersebut disebabkan kesalahan atau kelalaian berat PPK) sehubungan dengan klaim yang timbul dari hal-hal berikut terhitung sejak Tanggal Mulai Kerja sampai dengan tanggal penandatanganan berita acara penyerahan akhir:</span>
														</li>
													</ol>
													<ol>
														<li>
															<span>kehilangan atau kerusakan peralatan dan harta benda penyedia, dan Personil;</span>
